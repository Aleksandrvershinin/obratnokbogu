        <p><b>4)</b> Делаем ачаман: Берем ложечку в правую руку - окропляем левую ладонь. Берем ложечку в левую ладонь - капаем несколько капель на правую ладонь, смотрим на нее и говорим - <b>Ом Кешавая намаха </b>– и выпиваем ее через ложбинку в основании ладони. Далее, мы капаем еще раз, говорим: - <b>Ом Нараяна намаха</b> – выпиваем, Капаем в третий раз, говорим: - <b>Ом Мадхавайа намаха</b>- выпиваем. Капаем еще раз, говорим: - <b>Ом Говиндайа намаха</b> – и стряхиваем воду. Капаем уже на левую ладонь, говорим: - <b>Ом Вишнавэ намаха</b> – и стряхиваем воду.</p>

        <p><b>5)</b> Рядом с Туласи ставим изображение <b>истинного</b> Духовного учителя — Шрилы Прабхупады. Мы позиционируем себя как слуга слуги – даса даса анудаса. Мы являемся лишь инструментом в руках нашего Духовного учителя. И все, что мы будем предлагать – мы будем предлагать не сами, а именно через него.</p>

        <p><b>6)</b> Колокольчик. Берем из парча-татры ложечку воды, левой рукой берем колокольчик, Окропляем его и начинаем звонить. Это – начало нашей пуджи. Нужно звонить, пока мы не окончим Туласи-пуджу.</p>

        <p>
            <b> 7) </b>Поджигаем палочку благовония. Огнем с благовония поджигаем фитиль светильника с топленым маслом, и после сбиваем пламя благовония. Капаем каплю на правую ладонь, стряхиваем, потом окропляем благовоние и начинаем предлагать. Если у нас сложности с санскритом, мы можем сказать:
            <b> - Вот, дорогая Туласи, я хочу предложить тебе благовоние!</b> <br>
            На санскрите: - <b>Эша дхупаха ом Туласйаи намах!</b> И предлагаем благовоние Туласи : круговым движением от стоп Туласи – до верхушки кроны, по часовой стрелке, 7 раз. После чего можно предложить благовоние Духовному учителю, 3 раза, произнося: - <b>Ом Гураве намах!</b> И если присутствуют преданные, в их сторону 1 раз предлагаем: -<b>Ом вайшнавебхйо намах!</b>
        </p>

