            <p>Туласи почитается, как священное растение в ряде традиций индуизма в частности в вайшнавизме, где туласи поклоняются как воплощению Шримати Радхарани принявшей форму растения. Листья туласи ежедневно используются в обрядовой практике. Из веток туласи вытачивают бусы (кантхимала), которые являются священным атрибутом вайшнавов, и чётки (джапа-мала). В переводе с санскрита слово «туласи» означает «несравненная». Существует два вида туласи — тёмный, называемый Шьяма-туласи, или Кришна-туласи и светлый Рама-туласи.</p>

