        <h2 style="font-size: 24px;">История явления Шримати Туласи Деви</h2>
        <p>
            Согласно пуранической легенде, Туласи была одной из пастушек-гопи в вечной обители Кришны — локе Голоке. Она была близкой подругой Радхи — вечной возлюбленной Кришны и самой главной из гопи. Однажды Радха обнаружила, что Туласи без её разрешения играет с Кришной. Радха очень рассердилась и прокляла Туласи принять рождение на земле в человеческом теле. Однако, за всеми этими событиями стоял план Кришны, который заключался в том, чтобы принести благо обитателям материального мира, в том числе и людям на Земле. Услышав это проклятие, Туласи настолько испугалась, что приняла прибежище у Кришны. Кришна утешил её и попросил её не беспокоиться. Он пообещал ей, что если она примет человеческое тело и будет совершать аскезы, она вновь обретёт его. После этого Туласи родилась на земле как дочь царя Дхармараджи и его жены Мадхавати. Поскольку никто не мог сравниться с ней по красоте и качествам, она была названа «Туласи», что в переводе с санскрита означает «несравненная». Туласи отправилась в лес, и подвергла себя суровым аскезам, которые удовлетворили творца этой вселенной Брахму, пожелавшему даровать ей любые благословения.

        </p>
        <p>Туласи рассказала Брахме, что она была гопи на Голоке, и что она была проклята Радхой родиться в человеческом теле. Она также сказала ему, что когда она приняла прибежище Кришны, он уверил её в том, что совершая аскезы она сможет получить его назад. Узнав историю Туласи, Брахма даровал ей благословение, согласно которому она должна была выйти замуж за Кришну в одной из его ипостасей. Брахма рассказал Туласи о том, что Радха также прокляла одного из мальчиков-пастушков из Голоки по имени Судама, который принял рождение как демон Шанкхасура. Так как когда Туласи была на Голоке, Судама нравился ей, Брахма предложил ей выйти за него замуж. Брахма пообещал ей, что позже она должна получить благословение Нараяны и достичь освобождения. Брахма предсказал, что она станет величайшим из всех растений и самой дорогой преданной Нараяны, и что благодаря своей чистоте она освободит весь мир. Брахма также сказал, что ни одно поклонение без неё не будет считаться законченным. Затем Брахма дал Туласи особую «Радха-мантру», состоящую из 16 слогов и благословил её в том, что она станет так же удачлива, как сама Радха. Следуя указанию Брахмы, Туласи вышла замуж за демона Шанкхасуру.</p>

        <p>Шанкхасура обладал непревзойдённой силой, с помощью которой он завоевал райские планеты и сверг девов с небес. Обрёл он эту силу благодаря полученному благословению, которое заключалось в том, что пока его жена Туласи будет беззаветно верна ему, никто не сможет убить его. Обеспокоенные девы пришли на аудиенцию к Брахме и спросили у него совета о том, как они могут одолеть Шанкхасуру. Не зная как помочь им, Брахма отправился за помощью к Шиве, и Шива в свою очередь пошёл к Вишну на Вайкунтху. Вишну успокоил Шиву и пообещал исполнить желание Туласи получить его себе в мужья и сделать так, что Шанкхасура потеряет основанную на её верности мистическую силу. Вишну объявил, что для этого он примет обличье Шанкхасуры и когда Туласи, сама того не ведая, станет неверной своему мужу, девы получат возможность убить могущественного демона.</p>

        <p>Следуя своему обещанию, Нараяна принял форму Шанкхасуры и таким образом выполнил желание Туласи. Когда девы смогли убить демона Шанкхасуру, Туласи осознала, что кто-то другой, должно быть, пришёл к ней в облике её мужа и вступил с ней в связь. Осознав, что это, вероятно, был сам Нараяна, она прокляла его стать камнем. Однако, Нараяна в ответ благословил её, сказав, что Туласи, оставив своё нынешнее тело, станет ему такой же дорогой, как Лакшми. Тело Туласи превратится в реку Гандаки, а волосы — в кустарник туласи. Именно поэтому считается, что Нараяна обитает в реке Гандаки в форме камней, которые принимаются как его воплощения и называются «Шалаграма-шилами». Таким образом, Нараяна вечно связан с Туласи как в её форме реки Гандхаки, так и в её облике священного растения туласи. Демон Шанкхасура (который был на самом деле мальчиком-пастушком Судамой) стал раковиной Шанкхой, — одним из основных атрибутов Нараяны, всегда находящимся у него в руке. Считается, что без туласи поклонение Нараяне не может быть полным.</p>

        <h2 style="font-size: 32px;">Ритуал</h2>
        <p>Верующие люди уверены, что несчастья никогда не приходят в дом, в котором живет Туласи, а благодаря ее присутствию это место становится более чистым, чем все святые места. Горшочек, в котором растет Туласи, украшают юбочкой из красивой расшитой ткани. Также считается благоприятным поливать землю у основания Туласи, брать немного этой земли и касаться ею своего лба, если сделать так, человек обретает результат стодневного поклонения Богу.</p>
        <img style="float: left; width:350px; margin-right:20px;" src="/media/img/articles/shrimati-tulasi-devi2.jpg" alt="картинка">
        <p style="margin-top: 0;">Также листочками Туласи украшают стопы божеств в храмах. Из листьев и цветов растения плетут большие гирлянды, которыми украшают божеств, а потом эти гирлянды разделяются на части и раздаются прихожанам. Срывать листочки с Туласи нужно очень осторожно. В «Вишну-смрити» сказано: «Если во время сбора листьев Туласи кто-то ломает ветви, Господь чувствует боль в своем сердце. Согласно «Гандхарва-тантре», идеальными местами для медитации и поклонения являются заросли туласи. Вайшнавы (последователи Вишну или Кришны) особо почитают листья туласи, которые являются неотъемлемой частью подношений найведья — их кладут на пищу, приготовленную для Бога, а также к стопам мурти Вишну или Кришны. Говорится, что Кришна, или Вишну, не принимают предложенную им пищу, если в неё не был добавлен листочек туласи (в том случае, если есть возможность достать листья туласи).</p>
        <img style="float: left; width:350px; margin-right:20px;" src="/media/img/articles/shrimati-tulasi-devi3.jpg" alt="картинка">
        <p style="margin-top: 0;">Поклонение туласи в вайшнавизме — это настолько могущественный процесс, что просто поливая её или обходя вокруг неё, человек избавляется от самых тяжких грехов. Туласи поклоняются и в форме мурти (божества) — она изображается как девушка-пастушка в нарядных одеждах.</p>
        <h2 style="font-size: 32px;">Аромат Туласи</h2>
        <p>Туласи — съедобное растение. В ботанике его называют Базиликом священным. Листики Туласи имеют кисленький вкус. Зная о лечебных свойствах растения (потогонное, жаропонижающее, укрепляющее нервы, противоспазматическое, антибактериальное средство), его используют многие индийские (и не только) фармацевтические компании. <b>Однако верующие индийцы считают это оскорблением, они никогда не будут покупать такие лекарства.</b></p>
        <img style="float: left; width:350px; margin-right:20px;" src="/media/img/articles/shrimati-tulasi-devi4.jpg" alt="картинка">
        <p>При этом листья Туласи все же можно есть. Их обязательно кладут на пищу, которую предлагают Богу на алтаре. В писаниях говорится, что без этого он не примет даже крошки подношения. Потом вместе с предложенной пищей листья Туласи можно есть. В «Сканда-пуране» говорится, что, съев лист Туласи, человек становится безупречно чистым: «Даже грешник, лишенный духовной любви, может войти в обитель Господа, если просто съест предложенный Господу лист Туласи». Однако если кто-то съест не предложенный лист, то это равносильно греху убийства брахмана или половой связи с собственной матерью.</p>
        <img style="float: left; width:350px; margin-right:20px;" src="/media/img/articles/shrimati-tulasi-devi5.jpg" alt="картинка">
        <p>Когда деревце Туласи высыхает и умирает, из его ветвей и ствола вырезают бусины для четок (джапа-малы), на которых повторяют мантры. В шастрах говорится, что повторение имен Господа на четках из Туласи быстро дает освобождение от материального рабства. Также из Туласи делают бусы (кантхималы), которые носят на шее. Они защищают человека от дурных снов, происшествий, нападений с оружием. Но для того, чтобы эти бусы производили такой эффект, их обязательно нужно предложить божествам.</p>
        <h2 style="font-size: 24px;">Поклонение</h2>
        <img style="float: left; width:350px; margin-right:20px;" src="/media/img/articles/shrimati-tulasi-devi6.jpg" alt="картинка">
        <p> Происходит это так. Два небольших куста Туласи (для женской и мужской половин) вносят в горшках в алтарную залу и устанавливают на небольшие столики. Затем кто-то из прихожан проводит службу: деревцу предлагается огонь (зажженные ватные фитильки в топленом масле), благовония и цветы. Туласи украшается гирляндами из свежих цветов, обычно это бархатцы. В это время все остальные собравшиеся предлагают земной поклон Туласи и читают ее пранама-мантру (короткий стих, в котором восхваляются ее качества). Затем все поют мелодичные молитвы на бенгали под стук барабанов (мриданг) и звон медных тарелочек (каратал). После прихожане по очереди касаются правой ладонью огня, который был предложен Туласи, и своего лба, а также вдыхают аромат предложенных ей цветов. Затем все обходят минимум три раза вокруг Туласи: считается, что если делать так ежедневно, человек избавляется от всех грехов и оскорблений.</p>
