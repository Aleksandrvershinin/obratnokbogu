                                <h3 class="files__quotes__item_title">
                                    <a>Шрила Прабхупада о филантропии</a>
                                </h3>
                                <div class="files__quotes__item__box_text">
                                    <div class="files__quotes__item__text">
                                        <pre>
Поддержка больниц и светского образования, а также подобные виды деятельности, несомненно, благочестивы сами по себе, но они не имеют ничего общего с миссией сознания Кришны, которую должен распространять ИСККОН.

"ИСККОН (Международное общество сознания Кришны) – это некоммерческая организация, цель которой – приумножать благоденствие человеческого общества, направляя его внимание на Бога.
(Письмо Шрилы Прабхупады, 24.08.1968 г.)

"Люди высоко ценят, когда мы распространяем концепцию личного интереса с благополучия тела на благосостояние семьи, или с благосостояния семьи на благополучие общины, или с благополучия общины на благоденствие страны […] Но всё это – ложные идеи, ошибочные представления".
("В поисках просветления", глава 2)

"В писаниях рекомендуется благотворительность в гуне благости, однако благотворительность в гунах страсти и невежества не рекомендуется, поскольку это лишь напрасная трата денег. Материальную поддержку следует оказывать только распространению по всему миру сознания Кришны. Такова благотворительная деятельность в гуне благости".
("Бхагавад-гита как она есть", 16.1-3, комм.)

"Если вы даете деньги на открытие больницы, если даете деньги на открытие школ, обеспечиваете бесплатное образование. Это, несомненно, добродетельные поступки. Но и они предназначены для удовлетворения чувств. Предположим, я даю пожертвование на распространение образования. Тогда в следующей жизни я буду иметь хорошие возможности получить образование, я буду высокообразованным или, будучи образованным, займу хороший пост. В конечном итоге, какова идея всего этого? Если я получу хороший пост, если займу высокую должность, как я использую это? На чувственные наслаждения. […] Это кармическая деятельность".
(Лекция Шрилы Прабхупады, 14.02.1969 г.)

"Это очень важный момент. Людей очень интересует деятельность на благо человеческого общества. Они думают, что кормить бедных или раздавать одежду, или открывать больницы, школы, колледжи – "Эти вещи необходимы. Что толку слушать о Кришне?" У них такое мнение. Но подобная благотворительная деятельность – это расширенный эгоизм. […] Да. Не знают, в чем заключается настоящий личный интерес. Поэтому мы обнаруживаем много изъянов в такой благотворительной деятельности. […] Так что подобный вид филантропии не принимается в "Шримад-Бхагаватам" за высокое развитие цивилизации".
(Лекция Шрилы Прабхупады, 19.10.1972 г.)

"Благочестивая деятельность приносит материальное благополучие, но преданное служение нельзя обрести, сколько бы мы ни занимались материальной благочестивой деятельностью, подавали милостыню, открывали большие больницы и школы или посвящали свою деятельность филантропии. Достичь преданного служения можно только по милости чистого преданного".
("Шри Чайтанья-чаритамрита", Мадхья-лила 22.51, комм.)

"Открывать храмы или открывать школы, или подобная филантропическая деятельность – она, как правило, совершается теми, кто разочарован в этом митхйа* мире […] Потому что они посчитали, что это счастье лучше – уйти в политику, посвятить себя политическим движениям или открытию школ, больниц. Тогда почему они говорят, что этот джагат митхйа? * Всё это – деятельность материального мира".
(Лекция Шрилы Прабхупады, 9.11.1972 г.)
* иллюзорный мир

"Поэтому они занимаются этими приютами или открывают школу, или занимаются филантропией. Это другой вид материального удовольствия".
(беседа со Шрилой Прабхупадой, 5.08.1976 г.)

"Если вы станете сознающими Кршну […] тогда все другие обязанности будут исполнены автоматически. Не имеет смысла пытаться исполнять их отдельно – филантропия, философия и национализм, один "изм", другой "изм". Мы изобрели столько занятий и отвлеклись от исполнения настоящего долга".
(Лекция Шрилы Прабхупады, 15.01.1977 г.)

"Так называемые филантропия, альтруизм, гуманитарианизм, этот "изм", тот… Ложные… Не ложные – бесполезные. Это не поможет. Это благочестивая деятельность. Поэтому в "Чайтанья-чаритамрите" говорится: благочестивая или неблагочестивая – и то, и другое – препятствия для сознания Кршны. Так что нас не интересует даже благочестивая деятельность или неблагочестивая. Нас интересует, как служить Кршне".
(Беседа со Шрилой Прабхупадой, 8.01.1977 г.)

"Есть, для материалистичного человека есть два вида корыстного интереса. Один – это сконцентрированный интерес, а другой – расширенный интерес. […] Они думают, что я распространил свой личный интерес на свою страну, свою семью и стал великим человеком. И люди тоже оказывают ему почести. Мы знаем это по опыту, на практике, но это – расширенный эгоизм. Это не настоящая филантропия. Филантропия начинается тогда, когда мы становимся заинтересованными в интересе Кршны. Это и есть подлинная филантропия".
(Лекция Шрилы Прабхупады, 29.10.1976 г.)

