"Выполнять указания истинного духовного учителя – единственная обязанность ученика, именно в такой безоговорочной готовности исполнить любые указания истинного духовного учителя и кроется секрет успеха".
("Шримад-Бхагаватам", 2.9.8, комм.)

"Шрила Вишвaнaтхa Чакраварти тaкже подчеркивaет, что если мы хотим добиться успехa в попытке вернуться к Богу, то должны со всей серьезностью следовать указанию духовного учителя. Тaков путь к совершенству. Предaнный может не беспокоиться о том, достигнет он совершенствa или нет, тaк кaк, если он следует нaстaвлениям духовного учителя, он непременно достигнет его. Нaс должно зaботить только то, кaк исполнить укaзaние духовного учителя".
("Шримад-Бхагаватам", 4.8.71, комм.)

"Мы должны предаться гуру и служить ему. Не следует принимать любого человека в качестве гуру. Гуру должен быть представителем Кришны, тогда человек может предаться ему. Предание означает, что мы готовы принять все, что бы ни сказал гуру. Нельзя думать: "Мне нет дела до указания моего гуру. Я все равно останусь учеником". Такой подход не является настоящим принятием гуру. Конечно, сейчас стало модно принимать гуру именно так, но это никому не принесет блага".
("Учение Господа Капилы")

"Ученик или сын не имеет права говорить: "Это неверно. Я не стану этого делать". Тот, кто осмеливается сказать такое, – падший человек […] для ученика указание духовного учителя должно быть дороже жизни".
("Шримад-Бхагаватам", 3.24.13, комм.)

"Такое принятие слов духовного учителя называется шраута-вакьей. Это означает, что ученик должен неукоснительно исполнять указания духовного учителя. Шрила Вишванатха Чакраварти Тхакур в этой связи отмечает, что ученик должен принимать слова своего духовного учителя как свою жизнь и душу".
("Шри Чайтанья-чаритамрита", Ади-лила 7.72, комм.)

"Наказ духовного учителя является активным принципом духовной жизни. Любой, кто нарушает указание духовного учителя, немедленно становится бесполезным".
("Шри Чайтанья-чаритамрита", Ади-лила 12.10)

"Если ученики не придерживаются принципа следовать указанию духовного учителя, между ними немедленно возникают разногласия. Любое мнение, отличное от мнения духовного учителя, не имеет никакой ценности. Никто не должен пытаться вводить материальные представления в сферу духовной жизни. Это отклонение".
("Шри Чайтанья-чаритамрита", Ади-лила 12.9, комм.)

"Указания, полученные от духовного учителя, необходимо исполнять без промедления. Не следует отклоняться от них их или пытаться обойти".
("Шримад-Бхагаватам", 5.5.14, комм.)

"В первую очередь ради духовного развития нужно исполнять авторитетное указание духовного учителя. Так, по цепи парампары, человек может получить изначальную духовную силу, исходящую от Верховной Личности Бога (эвам парампара-праптам имам раджаршайо видух).
("Шримад-Бхагаватам", 8.15.28, комм.)

"Ученик должен неукоснительно следовать любым указаниям духовного учителя. Тот, кто выполняет это требование, лицезреет Верховную Личность Бога […] Вместо того чтобы пытаться увидеть Господа где-нибудь в зарослях Вриндавана, не отказываясь при этом от чувственных наслаждений, мы должны твердо придерживаться принципа следовать словам духовного учителя, и тогда нам будет совсем нетрудно увидеть Верховного Господа".
("Шримад-Бхагаватам", 4.28.51, комм.)

"Ученик, считающий себя независимым от духовного учителя и пренебрегающий его наставлениями, непременно потерпит неудачу".
("Шримад-Бхагаватам", 8.17.1, комм.)

"Брахма превозносит Кардаму Муни за то, что тот полностью выполнил указания духовного учителя, не обманывая и не ловча. […] Почитать духовного учителя – значит с безукоризненной точностью выполнять его указания".
("Шримад-Бхагаватам" 3.24.12, комм.)

